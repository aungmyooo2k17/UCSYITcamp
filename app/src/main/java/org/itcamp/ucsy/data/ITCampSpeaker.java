package org.itcamp.ucsy.data;

public class ITCampSpeaker {
    private String speakerName;
    private String speakerProfileUrl;
    private String speakerWork;

    public String getSpeakerName() {
        return speakerName;
    }

    public void setSpeakerName(String speakerName) {
        this.speakerName = speakerName;
    }

    public String getSpeakerProfileUrl() {
        return speakerProfileUrl;
    }

    public void setSpeakerProfileUrl(String speakerProfileUrl) {
        this.speakerProfileUrl = speakerProfileUrl;
    }

    public String getSpeakerWork() {
        return speakerWork;
    }

    public void setSpeakerWork(String speakerWork) {
        this.speakerWork = speakerWork;
    }
}
